let a = 10;
let b = 15;

// Addition
console.log(a + b);

// Subtraction
console.log(a - b);

// Multiplication
console.log(a * b);

// Division
console.log(a / b);

// Modulus
console.log(a % b);

// Exponentiation
console.log(a ** b); // a^b

// Increment
console.log(a); // 10
console.log(++a); // increment before output => 11
console.log(a++); // increment after output => 11
console.log(a); // 12

// Decrement
console.log(--b);
