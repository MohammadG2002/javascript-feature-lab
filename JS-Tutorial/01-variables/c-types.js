// variable types

// Number
let a = 1;
console.log(a);

// BigInt
a = BigInt(11231242342342341231231231);
console.log(a);

// String
a = "Hello";
console.log(a);

// Boolean
a = true;
console.log(a);

// Null
a = null;
console.log(a);

// Undefined
a = undefined;
console.log(a);

// Object
a = {};
console.log(a);

// Array
a = [];
console.log(a);

// Date
a = new Date();
console.log(a);

// Function
a = () => {};
console.log(a);

// Symbol
a = Symbol("id");
console.log(a);
