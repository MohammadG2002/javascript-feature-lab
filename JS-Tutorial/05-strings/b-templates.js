// `Back-Tics Syntax`
let a = `Hello`;
console.log(a);

// Quotes Inside Strings
a = `Hello "there"`;
console.log(a);

// Multiline Strings
a = `Hello
there`;
console.log(a);

// Interpolation
const b = "There";
a = `Hello ${b}`;
console.log(a);

// Expression Substitution
let c = 3;
a = `Max = ${c + 1}`;
console.log(a);
